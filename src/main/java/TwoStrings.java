public class TwoStrings {
    String lyricsPath;
    String songsPath;


    public TwoStrings(String lyricsPath, String songsPath){
        this.lyricsPath=lyricsPath;
        this.songsPath=songsPath;
    }

    public String getLyricsPath() {
        return lyricsPath;
    }

    public String getSongsPath() {
        return songsPath;
    }
}
